/**
 * Get chakras sorted by their scores (most imbalanced first)
 * @param {Object} scores - Chakra scores object
 * @returns {Array} Array of chakra objects with scores
 */
export const getChakrasByScore = (scores) => {
  return Object.entries(scores)
    .map(([chakraKey, score]) => ({
      key: chakraKey,
      ...chakraInfo[chakraKey],
      score,
      needsAttention: score < 5,
      balanceLevel: getBalanceLevel(score)
    }))
    .sort((a, b) => a.score - b.score);
};

/**
 * Get balance level description based on score
 * @param {number} score - Chakra score (0-10)
 * @returns {Object} Balance level info
 */
export const getBalanceLevel = (score) => {
  if (score >= 8) {
    return { level: 'excellent', description: 'Very balanced', color: '#27AE60' };
  } else if (score >= 6) {
    return { level: 'good', description: 'Mostly balanced', color: '#F39C12' };
  } else if (score >= 4) {
    return { level: 'fair', description: 'Somewhat imbalanced', color: '#E67E22' };
  } else if (score >= 2) {
    return { level: 'poor', description: 'Imbalanced', color: '#E74C3C' };
  } else {
    return { level: 'critical', description: 'Severely imbalanced', color: '#C0392B' };
  }
};

/**
 * Get chakra by name
 * @param {string} chakraName - Name of the chakra
 * @returns {Object} Chakra information
 */
export const getChakraInfo = (chakraName) => {
  return chakraInfo[chakraName] || null;
};

/**
 * Get all chakra names in order
 * @returns {Array} Array of chakra names
 */
export const getChakraOrder = () => {
  return ['root', 'sacral', 'solarPlexus', 'heart', 'throat', 'thirdEye', 'crown'];
};

/**
 * Get chakra color by name
 * @param {string} chakraName - Name of the chakra
 * @returns {string} Hex color code
 */
export const getChakraColor = (chakraName) => {
  return chakraInfo[chakraName]?.color || '#8B7EC8';
};

/**
 * Get meditation recommendations based on imbalanced chakras
 * @param {Array} imbalancedChakras - Array of imbalanced chakra names
 * @returns {Array} Array of meditation recommendations
 */
export const getMeditationRecommendations = (imbalancedChakras) => {
  return imbalancedChakras.map(chakraName => {
    const chakra = chakraInfo[chakraName];
    return {
      chakra: chakraName,
      name: chakra.name,
      duration: chakra.recommendedDuration,
      focus: chakra.meditationFocus,
      affirmation: chakra.affirmation,
      breathingPattern: chakra.breathingPattern,
      color: chakra.color,
      activities: chakra.healingActivities
    };
  });
};
